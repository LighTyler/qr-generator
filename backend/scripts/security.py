"""
Утилита для генерации секретных ключей.

Генерирует криптографически безопасный секретный ключ
для использования в JWT аутентификации или других целях.

Использование:
    python -m scripts.security
    
Вывод:
    SECRET_KEY = <64-символьный hex ключ>
    
Note:
    - Использует secrets.token_hex для криптографически безопасной генерации
    - 32 байта = 64 hex символа
    - Сгенерированный ключ следует добавить в .env файл
"""

import secrets


def generate_secret_key():
    """
    Генерация криптографически безопасного секретного ключа.
    
    Генерирует 32 байта случайных данных и преобразует в hex строку.
    
    Returns:
        str: 64-символьная hex строка
    """
    return secrets.token_hex(32)


if __name__ == "__main__":
    print(f"SECRET_KEY = {generate_secret_key()}")
